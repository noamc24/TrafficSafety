function hasGibberish(text) {
  return typeof text === "string" && /׳|�/.test(text);
}

Object.keys(PRODUCT_CATALOG).forEach((productId) => {
  const product = PRODUCT_CATALOG[productId];
  const clean = CLEAN_PRODUCT_TEXT[productId];
  if (clean) {
    product.title = clean.title;
    product.category = clean.category;
    product.shortDescription = clean.shortDescription;
    product.longDescription = clean.shortDescription;
  }

  if (Array.isArray(product.options)) {
    product.options = product.options.map((opt, optIndex) => {
      const optionName = hasGibberish(opt?.name) ? `אפשרות ${optIndex + 1}` : opt.name;
      const optionValues = Array.isArray(opt?.values)
        ? opt.values.map((val, valIndex) =>
            hasGibberish(val) ? `בחירה ${valIndex + 1}` : val
          )
        : [];

      return { name: optionName, values: optionValues };
    });
  }
});

function getProductIdFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const idFromQuery = params.get("id");
  if (idFromQuery) return idFromQuery;

  const idFromHash = new URLSearchParams((window.location.hash || "").replace(/^#/, "")).get("id");
  if (idFromHash) return idFromHash;

  return sessionStorage.getItem(LAST_PRODUCT_KEY);
}

function renderGallery(images, title) {
  const mainImage = document.getElementById("mainProductImage");
  const thumbsContainer = document.getElementById("productThumbs");
  if (!mainImage || !thumbsContainer) return;

  mainImage.src = images[0];
  mainImage.alt = title;
  thumbsContainer.innerHTML = "";

  images.forEach((src, index) => {
    const thumbBtn = document.createElement("button");
    thumbBtn.type = "button";
    thumbBtn.className = `product-gallery__thumb${index === 0 ? " active" : ""}`;
    thumbBtn.innerHTML = `<img src="${src}" alt="${title} - \u05ea\u05de\u05d5\u05e0\u05d4 ${index + 1}" />`;
    thumbBtn.addEventListener("click", () => {
      mainImage.src = src;
      thumbsContainer.querySelectorAll(".product-gallery__thumb").forEach((thumb) => thumb.classList.remove("active"));
      thumbBtn.classList.add("active");
    });
    thumbsContainer.appendChild(thumbBtn);
  });
}

function renderOptions(options) {
  const optionsContainer = document.getElementById("productOptions");
  if (!optionsContainer) return [];

  const fields = [];
  optionsContainer.innerHTML = "";

  options.forEach((option, index) => {
    const wrapper = document.createElement("div");
    wrapper.className = "product-option";

    const fieldId = `productOption${index}`;
    wrapper.innerHTML = `
      <label for="${fieldId}">${option.name}</label>
      <select id="${fieldId}" class="form-select"></select>
    `;

    const select = wrapper.querySelector("select");
    option.values.forEach((value) => {
      const opt = document.createElement("option");
      opt.value = value;
      opt.textContent = value;
      select.appendChild(opt);
    });

    fields.push({ label: option.name, element: select });
    optionsContainer.appendChild(wrapper);
  });

  // Conditional custom text input for personalized wording
  const textOptionField = fields.find((field) => field.label === "\u05e0\u05d5\u05e1\u05d7");
  if (textOptionField) {
    const customWrapper = document.createElement("div");
    customWrapper.className = "product-option";
    customWrapper.id = "customTextOptionWrapper";
    customWrapper.style.display = "none";
    customWrapper.innerHTML = `
      <label for="customTextOptionInput">\u05d4\u05e7\u05dc\u05d3 \u05e0\u05d5\u05e1\u05d7 \u05de\u05d5\u05ea\u05d0\u05dd</label>
      <input id="customTextOptionInput" type="text" class="form-control" placeholder="\u05d4\u05db\u05e0\u05e1 \u05d0\u05ea \u05d4\u05e0\u05d5\u05e1\u05d7 \u05d4\u05e8\u05e6\u05d5\u05d9" />
    `;
    optionsContainer.appendChild(customWrapper);

    const toggleCustomTextInput = () => {
      customWrapper.style.display =
        textOptionField.element.value === "\u05de\u05d5\u05ea\u05d0\u05dd \u05d0\u05d9\u05e9\u05d9\u05ea" ? "block" : "none";
    };

    textOptionField.element.addEventListener("change", toggleCustomTextInput);
    toggleCustomTextInput();
  }

  const quantityWrapper = document.createElement("div");
  quantityWrapper.className = "product-option";
  quantityWrapper.innerHTML = `
    <label for="productQty">\u05db\u05de\u05d5\u05ea</label>
    <select id="productQty" class="form-select">
      <option value="1">1</option>
      <option value="2">2</option>
      <option value="3">3</option>
      <option value="4">4</option>
      <option value="5">5</option>
    </select>
  `;
  optionsContainer.appendChild(quantityWrapper);

  return fields;
}

function setupAddToCart(productId, product, optionFields) {
  const btn = document.getElementById("addToCartBtn");
  const feedback = document.getElementById("addToCartFeedback");
  const qtyField = document.getElementById("productQty");
  if (!btn || !feedback || !qtyField) return;

  btn.addEventListener("click", () => {
    const selectedOptions = optionFields.map((field) => ({
      name: field.label,
      value: field.element.value
    }));

    const customTextInput = document.getElementById("customTextOptionInput");
    const customTextValue = customTextInput ? customTextInput.value.trim() : "";
    if (customTextValue) {
      selectedOptions.push({
        name: "\u05e0\u05d5\u05e1\u05d7 \u05de\u05d5\u05ea\u05d0\u05dd",
        value: customTextValue
      });
    }

    const quantity = Number(qtyField.value || 1);
    const cart = JSON.parse(localStorage.getItem(PRODUCT_CART_STORAGE_KEY) || "[]");
    const nextItem = {
      productId,
      title: product.title,
      category: product.category,
      image: (product.images && product.images[0]) ? product.images[0] : "/assets/Icons/TSCLogoSquared.png",
      quantity,
      options: selectedOptions,
      addedAt: new Date().toISOString()
    };
    const existing = cart.find((item) => {
      if (item.productId !== nextItem.productId) return false;
      const a = item.options || [];
      const b = nextItem.options || [];
      if (a.length !== b.length) return false;
      return a.every((opt, idx) => opt.name === b[idx]?.name && opt.value === b[idx]?.value);
    });

    if (existing) {
      existing.quantity = (Number(existing.quantity) || 1) + nextItem.quantity;
    } else {
      cart.push(nextItem);
    }

    localStorage.setItem(PRODUCT_CART_STORAGE_KEY, JSON.stringify(cart));
    window.dispatchEvent(new Event("tsc-cart-updated"));

    feedback.textContent = "\u05d4\u05de\u05d5\u05e6\u05e8 \u05e0\u05d5\u05e1\u05e3 \u05dc\u05e2\u05d2\u05dc\u05d4";
    setTimeout(() => {
      feedback.textContent = "";
    }, 2200);
  });
}

function renderNotFound() {
  const main = document.getElementById("main-content");
  if (!main) return;
  main.innerHTML = `
    <div class="alert alert-warning">
      \u05d4\u05de\u05d5\u05e6\u05e8 \u05dc\u05d0 \u05e0\u05de\u05e6\u05d0. \u05e0\u05d9\u05ea\u05df \u05dc\u05d7\u05d6\u05d5\u05e8 \u05dc<a href="/store" class="alert-link">\u05e2\u05de\u05d5\u05d3 \u05d4\u05d7\u05e0\u05d5\u05ea</a>.
    </div>
  `;
}

document.addEventListener("DOMContentLoaded", () => {
  const productId = getProductIdFromUrl();
  const product = PRODUCT_CATALOG[productId];
  if (!productId || !product) {
    renderNotFound();
    return;
  }

  sessionStorage.setItem(LAST_PRODUCT_KEY, productId);

  const categoryEl = document.getElementById("productCategory");
  const titleEl = document.getElementById("productTitle");
  const shortDescEl = document.getElementById("productShortDesc");
  const longDescEl = document.getElementById("productLongDesc");

  if (!categoryEl || !titleEl || !shortDescEl || !longDescEl) return;

  document.title = `${product.title} | \u05d4\u05de\u05e8\u05db\u05d6 \u05dc\u05d1\u05d8\u05d9\u05d7\u05d5\u05ea \u05d1\u05ea\u05e0\u05d5\u05e2\u05d4`;
  categoryEl.textContent = product.category;
  titleEl.textContent = product.title;
  shortDescEl.textContent = product.shortDescription;
  longDescEl.textContent = product.longDescription;

  renderGallery(product.images, product.title);
  const optionFields = renderOptions(product.options || []);
  setupAddToCart(productId, product, optionFields);
});


